import math

# x values
x = [[0, 0],
     [0.57735, -0.57735,   -0.57735, -0.57735,
      0.57735,  0.57735,   -0.57735, 0.57735],
     [0.774597, -0.774597,   0, -0.774597,   -0.774597, -0.774597,
      0.774597, 0,           0, 0,           -0.774597, 0,
      0.774597, 0.774597,    0, 0.774597,    -0.774597, 0.774597]]

# coefficients
w = [[4],
     [1, 1,
      1, 1],
     [0.308642, 0.493827, 0.308642,
      0.493827, 0.790123, 0.493827,
      0.308642, 0.493827, 0.308642]]


def Sumlet(x1, x2, w):
    # return w * (0.1*x1**2 + 0.2*math.cos(4*x1*x2) + 2)
    return w * ((x1+1)**3+0.2*math.cos(x1+x2))


def Sum(n):
    s = 0
    for i in range(n**2):
        s += Sumlet(x[n-1][i*2], x[n-1][i*2+1], w[n-1][i])
    return s


# Range [1, 3]
